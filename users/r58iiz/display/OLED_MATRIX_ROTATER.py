def rotate_glyph_270(glyph):
    # glyph: 8 bytes, each byte = 8 bits wide (one row)
    out = [0] * 8
    for y in range(8):
        for x in range(8):
            bit = (glyph[y] >> x) & 1
            out[7 - x] |= bit << y
    return out


def rotate_tiles_270_11x4(flat_glyphs):
    if len(flat_glyphs) != 44 * 8:
        raise ValueError("Expected 352 bytes")

    GLYPHS_W = 11
    GLYPHS_H = 4
    BYTES_PER_GLYPH = 8

    glyphs = [
        flat_glyphs[i * BYTES_PER_GLYPH : (i + 1) * BYTES_PER_GLYPH]
        for i in range(GLYPHS_W * GLYPHS_H)
    ]

    grid = [glyphs[y * GLYPHS_W : (y + 1) * GLYPHS_W] for y in range(GLYPHS_H)]

    out = []
    for col in range(GLYPHS_W):
        for row in reversed(range(GLYPHS_H)):
            out.extend(rotate_glyph_270(grid[row][col]))

    return out


